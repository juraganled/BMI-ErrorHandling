import Foundation

struct BMI {
    var weight: Double = 0
    var height: Double = 0
    var bmi: Double = 0
    var weightAdjustment: Double = 0
    var resultTitle: String = ""
    var resultDescription: String = ""
    
}

enum BMIError: Error {
    case weightNonNumber
    case heightNonNumber
    case weightZero
    case heightZero
    case weightHeightZero
    case negativeOutcome
}
